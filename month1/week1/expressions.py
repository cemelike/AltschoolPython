# Expressions are combinations of variables, values, and operators that produce a result.
# Examples of expressions include "x + y" or "z == 5".

number_of_students = 100  # This is an expression
number_of_staff = 20  # This is an expression
total_number_of_people = number_of_students + number_of_staff  # This is an expression
print(total_number_of_people)  # This will print 120

