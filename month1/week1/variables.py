# Variables are used to store data, such as numbers or text
# In Python. Variables must be defined before they can be used.

# Let's define some variables
first_name = "John"
last_name = "Adeyemi"
age = 30
height = 1.8
is_married = False

# You can print the value of a variable using the print() function
print(first_name)
print(last_name)

# You can also print multiple variables in one line
print(first_name, last_name)

# You can also print text and variables in one line
print("My name is", first_name, last_name)

# You can also print text and variables in one line using f-strings
print(f"My name is {first_name} {last_name} and I am {age} years old")

