# The input() function is used to read input from the user in Python.
# The input() function reads the user input as a string.

# The input() function takes a single argument that is a string,
# called the prompt, which is printed on the screen before the user input is read.

# Example: Read an input from the user and print it.
name = input("Enter your name: ")
print(name)

# Example: Read two inputs from the user and print them.
name = input("Enter your name: ")
age = input("Enter your age: ")
print(f"Hello {name}! You are {age} years old.")
